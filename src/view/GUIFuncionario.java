    private void cadastrarFuncionario() {
        try {
            // Telefone
            TelefoneVO tVO = new TelefoneVO();
            tVO.setFone(jtfTelefone.getText());
            TelefoneServicos ts = services.ServicosFactory.getTelefoneServicos();
            ts.cadadtrarTelefone(tVO);
            int id_fone = ts.getidTelefone(tVO);

            // Endereço
            EnderecoVO eVO = new EnderecoVO();
            eVO.setRua(jtfRua.getText());
            eVO.setNumero(jtfNumero.getText());
            eVO.setComplemento(jtfComplemento.getText());
            eVO.setBairro(jtfBairro.getText());
            eVO.setCidade(jtfCidade.getText());
            eVO.setCep(jtfCEP.getText());
            EnderecoServicos es = services.ServicosFactory.getEnderecoServicos();
            es.cadadtrarEndereco(eVO);
            int id_endereco = es.getidTelefone(eVO);

            // Funcionário
            FuncionarioVO fVO = new FuncionarioVO();
            fVO.setNome_funcionario(jtfNome.getText());
            fVO.setEmail_funcionario(jtfEmail.getText());
            FuncionarioServicos fs = services.ServicosFactory.getFuncionarioServicos();
            fs.cadastrarFuncionario(fVO, id_fone, id_endereco);

            JOptionPane.showMessageDialog(null, "Funcionário cadastrado com sucesso!");
            
            limpar();
            preencherTabela();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar: " + e.getMessage());
        }
    }

    private void preencherTabela() {
        try {
            dtm.setNumRows(0);
            idsFuncionarios.clear();

            FuncionarioServicos fs = services.ServicosFactory.getFuncionarioServicos();
            TelefoneServicos ts = services.ServicosFactory.getTelefoneServicos();
            EnderecoServicos es = services.ServicosFactory.getEnderecoServicos();

            ArrayList<FuncionarioVO> funcionarios = fs.buscarFuncionario();

            for (FuncionarioVO f : funcionarios) {
                TelefoneVO tVO = ts.buscarTelefonePorId((int) f.getId_fone());
                EnderecoVO eVO = es.buscarEnderecoPorId((int) f.getId_endereco());

                dtm.addRow(new Object[]{
                    f.getNome_funcionario(),
                    f.getEmail_funcionario(),
                    tVO.getFone(),
                    eVO.getRua(),
                    eVO.getNumero(),
                    eVO.getBairro(),
                    eVO.getCidade(),
                    eVO.getCep()
                });

                idsFuncionarios.add(f.getId_funcionario());
            }
            jtFuncionario.setModel(dtm);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher tabela: " + e.getMessage());
        }
    }
    
    private void filtrar() {
        try {
            String nome = jtfPesquisar.getText().trim();
            dtm.setNumRows(0);
            idsFuncionarios.clear();

            if (nome.isEmpty()) {
                preencherTabela();
            } else {
                FuncionarioServicos fs = services.ServicosFactory.getFuncionarioServicos();
                TelefoneServicos ts = services.ServicosFactory.getTelefoneServicos();
                EnderecoServicos es = services.ServicosFactory.getEnderecoServicos();

                String query = "WHERE nome_funcionario LIKE '%" + nome + "%'";
                ArrayList<FuncionarioVO> funcionarios = fs.filtrarFuncionario(query);

                for (FuncionarioVO f : funcionarios) {
                    TelefoneVO tVO = ts.buscarTelefonePorId((int) f.getId_fone());
                    EnderecoVO eVO = es.buscarEnderecoPorId((int) f.getId_endereco());

                    String enderecoCompleto = String.format("%s, %s - %s, %s",
                        eVO.getRua(), eVO.getNumero(), eVO.getBairro(), eVO.getCidade()
                    );
                    if (eVO.getComplemento() != null && !eVO.getComplemento().isEmpty()) {
                        enderecoCompleto += " (" + eVO.getComplemento() + ")";
                    }

                    dtm.addRow(new Object[]{
                        f.getNome_funcionario(),
                        f.getEmail_funcionario(),
                        tVO.getFone(),
                        enderecoCompleto
                    });

                    idsFuncionarios.add(f.getId_funcionario()); // ✅
                }
                jtFuncionario.setModel(dtm);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao filtrar: " + e.getMessage());
        }
    }
    
    private void deletarFuncionario() {
        try {
            int linha = jtFuncionario.getSelectedRow();
            if (linha == -1) {
                JOptionPane.showMessageDialog(this, "Selecione um funcionário!");
                return;
            }

            if (idsFuncionarios.isEmpty() || linha >= idsFuncionarios.size()) {
                JOptionPane.showMessageDialog(this, "Dados inconsistentes.");
                preencherTabela();
                return;
            }

            int id_funcionario = idsFuncionarios.get(linha); 

            int confirm = JOptionPane.showConfirmDialog(this, "Excluir funcionário?", "Confirmação", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                FuncionarioServicos fs = services.ServicosFactory.getFuncionarioServicos();
                fs.deletarFuncionario(id_funcionario);
                JOptionPane.showMessageDialog(this, "Funcionário excluído!");
                preencherTabela();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao deletar: " + e.getMessage());
        }
    }
    
    private void preencherAlterarFuncionario() {
        try {
            int linha = jtFuncionario.getSelectedRow();
            if (linha == -1) {
                JOptionPane.showMessageDialog(this, "Selecione um funcionário!");
                return;
            }

            if (idsFuncionarios.isEmpty() || linha >= idsFuncionarios.size()) {
                JOptionPane.showMessageDialog(this, "Dados inconsistentes.");
                return;
            }

            idFuncionarioEmEdicao = idsFuncionarios.get(linha); 

            jTabbedPane1.setSelectedIndex(0);

            FuncionarioServicos fs = services.ServicosFactory.getFuncionarioServicos();
            FuncionarioVO fVO = fs.buscarFuncionarioPorId(idFuncionarioEmEdicao);

            TelefoneServicos ts = services.ServicosFactory.getTelefoneServicos();
            EnderecoServicos es = services.ServicosFactory.getEnderecoServicos();

            TelefoneVO tVO = ts.buscarTelefonePorId((int) fVO.getId_fone());
            EnderecoVO eVO = es.buscarEnderecoPorId((int) fVO.getId_endereco());

            jtfNome.setText(fVO.getNome_funcionario());
            jtfEmail.setText(fVO.getEmail_funcionario());
            jtfTelefone.setText(tVO.getFone());
            jtfRua.setText(eVO.getRua());
            jtfNumero.setText(eVO.getNumero());
            jtfComplemento.setText(eVO.getComplemento() != null ? eVO.getComplemento() : "");
            jtfBairro.setText(eVO.getBairro());
            jtfCidade.setText(eVO.getCidade());
            jtfCEP.setText(eVO.getCep());

            jbtnCadastrar.setEnabled(false);
            jbtnAlterar.setEnabled(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao preencher: " + e.getMessage());
        }
    }
    
    private void confirmarAlterarFuncionario() {
        try {
            if (idFuncionarioEmEdicao == -1) {
                JOptionPane.showMessageDialog(this, "Nenhum funcionário selecionado para alteração.");
                return;
            }

            String nome = jtfNome.getText().trim();
            String email = jtfEmail.getText().trim();
            if (nome.isEmpty() || email.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha nome e e-mail!");
                return;
            }

            FuncionarioVO fVO = new FuncionarioVO();
            fVO.setNome_funcionario(nome);
            fVO.setEmail_funcionario(email);

            TelefoneVO tVO = new TelefoneVO();
            tVO.setFone(jtfTelefone.getText().trim());

            EnderecoVO eVO = new EnderecoVO();
            eVO.setRua(jtfRua.getText().trim());
            eVO.setNumero(jtfNumero.getText().trim());
            eVO.setComplemento(jtfComplemento.getText().trim());
            eVO.setBairro(jtfBairro.getText().trim());
            eVO.setCidade(jtfCidade.getText().trim());
            eVO.setCep(jtfCEP.getText().trim());

            FuncionarioServicos fs = services.ServicosFactory.getFuncionarioServicos();
            fs.confirmarAlteracao(fVO, tVO, eVO, idFuncionarioEmEdicao); // ✅

            JOptionPane.showMessageDialog(this, "Funcionário alterado com sucesso!");

            limpar();
            idFuncionarioEmEdicao = -1;
            jbtnCadastrar.setEnabled(true);
            jbtnAlterar.setEnabled(false);

            preencherTabela();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao alterar: " + e.getMessage());
        }
    }

