    private void cadastrarReserva() {
        try {
            String dataStr = jtfDataReserva.getText().trim();
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            sdf.setLenient(false);
            Date dataReserva = sdf.parse(dataStr);

            if (jcbCliente.getSelectedItem() == null || jcbLivro.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(this, "Selecione cliente e livro!");
                return;
            }

            ClienteVO cliente = (ClienteVO) jcbCliente.getSelectedItem();
            LivroVO livro = (LivroVO) jcbLivro.getSelectedItem();

            LivroServicos ls = ServicosFactory.getLivroServicos();
            ArrayList<LivroVO> livrosDisponiveis = ls.buscarLivrosComFiltro("", "Disponível");
            boolean livroDisponivel = livrosDisponiveis.stream()
                .anyMatch(l -> l.getIdLivro() == livro.getIdLivro());

            if (livroDisponivel) {
                JOptionPane.showMessageDialog(this,
                    "Reserva permitida apenas para livros **indisponíveis**!\n" +
                    "Este livro está disponível para empréstimo.",
                    "Livro Disponível",
                    JOptionPane.WARNING_MESSAGE
                );
                return;
            }

            ReservaVO rVO = new ReservaVO();
            rVO.setDtReserva(dataReserva);
            rVO.setIdCliente((int) cliente.getId_cliente());
            rVO.setIdLivro(livro.getIdLivro());

            ReservaServicos rs = ServicosFactory.getReservaServicos();
            rs.cadastrarReserva(rVO);

            JOptionPane.showMessageDialog(this, "Reserva cadastrada com sucesso!");
            limpar();
            preencherTabela();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar reserva: " + e.getMessage());
        }
    }
    
    private void preencherTabela() {
        try {
            dtm.setNumRows(0);
            idsReservas.clear();

            ReservaServicos rs = ServicosFactory.getReservaServicos();
            ArrayList<ReservaVO> reservas = rs.buscarReservas();

            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            for (ReservaVO r : reservas) {
                String nomeLivroExibicao = r.getNomeLivro();
                try {
                    LivroServicos ls = ServicosFactory.getLivroServicos();
                    LivroVO livroVO = ls.buscarLivroPorId(r.getIdLivro());
                    if (livroVO != null) {
                        nomeLivroExibicao = livroVO.toString();
                    }
                } catch (Exception ex) {
                    nomeLivroExibicao = r.getNomeLivro() + " (erro ao carregar)";
                }

                dtm.addRow(new Object[]{
                    r.getNomeCliente(),
                    nomeLivroExibicao,
                    sdf.format(r.getDtReserva()),
                    r.getStatusReserva()
                });
                idsReservas.add(r.getIdReserva());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao preencher tabela: " + e.getMessage());
        }
    }
    
     private void filtrar() {
        try {
            String termo = jtfPesquisar.getText().trim();
            String tipo = (String) jcbTipoPesquisa.getSelectedItem();
            String statusSelecionado = (String) jcbStatusFiltro.getSelectedItem();
            String statusFiltro = "Todos".equals(statusSelecionado) ? null : statusSelecionado;

            dtm.setNumRows(0);
            idsReservas.clear();

            ReservaServicos rs = ServicosFactory.getReservaServicos();
            ArrayList<ReservaVO> reservas;

            if (termo.isEmpty()) {
                if (statusFiltro == null) {
                    reservas = rs.buscarReservas();
                } else {
                    reservas = rs.buscarReservasPorStatus(statusFiltro);
                }
            } else {
                if ("Cliente".equals(tipo)) {
                    reservas = rs.filtrarPorCliente(termo, statusFiltro);
                } else {
                    reservas = rs.filtrarPorLivro(termo, statusFiltro);
                }
            }

            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            for (ReservaVO r : reservas) {
                String dtReservaStr = r.getDtReserva() != null ? sdf.format(r.getDtReserva()) : "";
                String nomeLivroExibicao = r.getNomeLivro();
                try {
                    LivroServicos ls = ServicosFactory.getLivroServicos();
                    LivroVO livroVO = ls.buscarLivroPorId(r.getIdLivro());
                    if (livroVO != null) {
                        nomeLivroExibicao = livroVO.toString();
                    }
                } catch (Exception ex) {
                    // mantém o nome original
                }
                dtm.addRow(new Object[]{
                    r.getNomeCliente(),
                    nomeLivroExibicao,
                    dtReservaStr,
                    r.getStatusReserva()
                });
                idsReservas.add(r.getIdReserva());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao filtrar: " + e.getMessage());
        }
    }
    
    private void preencherAlterar() {
        try {
            int linha = jtReserva.getSelectedRow();
            if (linha == -1) {
                JOptionPane.showMessageDialog(this, "Selecione uma reserva!");
                return;
            }

            if (idsReservas.isEmpty() || linha >= idsReservas.size()) {
                JOptionPane.showMessageDialog(this, "Dados inconsistentes.");
                return;
            }

            idReservaEmEdicao = idsReservas.get(linha);

            ReservaServicos rs = ServicosFactory.getReservaServicos();
            ReservaVO rVO = rs.buscarReservaPorId(idReservaEmEdicao);

            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            jtfDataReserva.setText(rVO.getDtReserva() != null ? sdf.format(rVO.getDtReserva()) : "");
            jtfDataReserva.setEditable(true);

            carregarClientes("");
            carregarLivros("");

            // Seleciona cliente
            for (int i = 0; i < jcbCliente.getItemCount(); i++) {
                Object item = jcbCliente.getItemAt(i);
                if (item instanceof ClienteVO && ((ClienteVO) item).getId_cliente() == rVO.getIdCliente()) {
                    jcbCliente.setSelectedIndex(i);
                    break;
                }
            }

            // Seleciona livro
            for (int i = 0; i < jcbLivro.getItemCount(); i++) {
                Object item = jcbLivro.getItemAt(i);
                if (item instanceof LivroVO && ((LivroVO) item).getIdLivro() == rVO.getIdLivro()) {
                    jcbLivro.setSelectedIndex(i);
                    break;
                }
            }

            jTabbedPane1.setSelectedIndex(0);
            jbtnCadastrar.setEnabled(false);
            jbtnAlterar.setEnabled(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao preencher dados: " + e.getMessage());
        }
    }
    
     private void confirmarAlterar() {
        try {
            if (idReservaEmEdicao == -1) {
                JOptionPane.showMessageDialog(this, "Nenhuma reserva selecionada para alteração.");
                return;
            }

            String dataReservaStr = jtfDataReserva.getText().trim();
            if (dataReservaStr.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha a data da reserva!");
                return;
            }

            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            sdf.setLenient(false);
            Date dtReserva = sdf.parse(dataReservaStr);

            ClienteVO cliente = (ClienteVO) jcbCliente.getSelectedItem();
            LivroVO livro = (LivroVO) jcbLivro.getSelectedItem();

            if (cliente == null || livro == null) {
                JOptionPane.showMessageDialog(this, "Selecione cliente e livro!");
                return;
            }

            // ✅ Validar novamente: só permite livros indisponíveis
            LivroServicos ls = ServicosFactory.getLivroServicos();
            ArrayList<LivroVO> livrosDisponiveis = ls.buscarLivrosComFiltro("", "Disponível");
            boolean livroDisponivel = livrosDisponiveis.stream()
                .anyMatch(l -> l.getIdLivro() == livro.getIdLivro());

            if (livroDisponivel) {
                JOptionPane.showMessageDialog(this,
                    "Reserva só pode ser feita para livros indisponíveis!",
                    "Livro Disponível",
                    JOptionPane.WARNING_MESSAGE
                );
                return;
            }
            ReservaVO rVO = new ReservaVO();
            rVO.setIdReserva(idReservaEmEdicao);
            rVO.setDtReserva(dtReserva);
            rVO.setIdCliente((int) cliente.getId_cliente());
            rVO.setIdLivro(livro.getIdLivro());

            ReservaServicos rs = ServicosFactory.getReservaServicos();
            rs.atualizarReserva(rVO);

            JOptionPane.showMessageDialog(this, "Reserva alterada com sucesso!");
            limpar();
            preencherTabela();

        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Formato de data inválido. Use dd/MM/yyyy.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao alterar: " + e.getMessage());
        }
    }
    
    private void cancelarReserva() {
        try {
            int linha = jtReserva.getSelectedRow();
            if (linha == -1) {
                JOptionPane.showMessageDialog(this, "Selecione uma reserva!");
                return;
            }

            if (idsReservas.isEmpty() || linha >= idsReservas.size()) {
                JOptionPane.showMessageDialog(this, "Dados inconsistentes.");
                return;
            }

            int idReserva = idsReservas.get(linha);
            int confirma = JOptionPane.showConfirmDialog(this,
                "Cancelar esta reserva?", "Confirmação", JOptionPane.YES_NO_OPTION);

            if (confirma == JOptionPane.YES_OPTION) {
                ReservaServicos rs = ServicosFactory.getReservaServicos();
                ReservaVO rVO = rs.buscarReservaPorId(idReserva);
                rVO.setStatusReserva("Cancelada");
                rs.atualizarStatusReserva(rVO); 

                JOptionPane.showMessageDialog(this, "Reserva cancelada!");
                preencherTabela();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao cancelar: " + e.getMessage());
        }
    }
    
    private void converterReservaEmEmprestimo() {
        try {
            int linha = jtReserva.getSelectedRow();
            if (linha == -1) {
                JOptionPane.showMessageDialog(this, "Selecione uma reserva!");
                return;
            }

            if (idsReservas.isEmpty() || linha >= idsReservas.size()) {
                JOptionPane.showMessageDialog(this, "Dados inconsistentes.");
                return;
            }

            int idReserva = idsReservas.get(linha);
            ReservaServicos rs = ServicosFactory.getReservaServicos();
            ReservaVO rVO = rs.buscarReservaPorId(idReserva);

            if (!"Ativa".equals(rVO.getStatusReserva())) {
                JOptionPane.showMessageDialog(this, "Apenas reservas 'Ativas' podem ser convertidas.");
                return;
            }

            int confirma = JOptionPane.showConfirmDialog(
                this,
                "Deseja criar um empréstimo para esta reserva?",
                "Confirmar Conversão",
                JOptionPane.YES_NO_OPTION
            );
            if (confirma != JOptionPane.YES_OPTION) {
                return;
            }

            ClienteServicos cs = ServicosFactory.getClienteServicos();
            LivroServicos ls = ServicosFactory.getLivroServicos();
            ClienteVO cliente = cs.buscarClientePorId(rVO.getIdCliente());
            LivroVO livro = ls.buscarLivroPorId(rVO.getIdLivro());

            Runnable callback = () -> SwingUtilities.invokeLater(this::preencherTabela);

            GUIEmprestimo guiEmp = new GUIEmprestimo(cliente, livro, rVO, callback);

            JDesktopPane desktop = (menuPrincipal != null) ? menuPrincipal.getDesktop() : (JDesktopPane) getParent();
            desktop.add(guiEmp);
            guiEmp.setVisible(true);
            guiEmp.toFront();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao preparar conversão: " + e.getMessage());
        }
    }
    
