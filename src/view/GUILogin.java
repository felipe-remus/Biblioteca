    private void logar() {
        try {
            LoginVO lVO = new LoginVO();
            LoginServicos ls = services.ServicosFactory.getLoginServicos();

            lVO.setLogin(jtfLogin.getText().trim());
            lVO.setSenha(jpfSenha.getText().trim());
            lVO.setPerfil(ls.getPerfil((String) jcbPerfil.getSelectedItem()));

            LoginVO usuarioAutenticado = ls.autenticarLogin(lVO);

            if (usuarioAutenticado != null) {
                SessaoUsuario.setUsuarioLogado(usuarioAutenticado);
                GUIMenuPrincipal gmp = new GUIMenuPrincipal();
                gmp.setVisible(true);
                
                // ‚úÖ Se for cliente, abre diretamente a pesquisa de livros
                if (usuarioAutenticado.getPerfil() == 1) { // ID 1 = Cliente
                    GUILivro guiLivroCliente = new GUILivro(true); // true = modo cliente
                    gmp.jdpAreaDeTrabalho.add(guiLivroCliente);
                    guiLivroCliente.setVisible(true);
                    guiLivroCliente.toFront();
                }
                
                dispose();
            } else {
                // üîí Mensagem gen√©rica de seguran√ßa
                JOptionPane.showMessageDialog(null, "Login, senha ou perfil inv√°lidos.", 
                    "Erro de Autentica√ß√£o", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            // üîí Tamb√©m usa mensagem gen√©rica em caso de erro
            JOptionPane.showMessageDialog(null, "Login, senha ou perfil inv√°lidos.", 
                "Erro de Autentica√ß√£o", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void restaurarPerfilComboBox() {
        try {
            LoginServicos ls = services.ServicosFactory.getLoginServicos();
            List<Map<String, Object>> perfis = ls.listaPerfil();

            jcbPerfil.removeAllItems();
            codperfil.clear();

            for (Map<String, Object> perfil : perfis) {
                codperfil.addElement((Integer) perfil.get("id"));
                jcbPerfil.addItem((String) perfil.get("nome"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar perfis: " + e.getMessage());
        }
    }
    
    private boolean isPrimeiraExecucao() {
        try {
            LoginServicos ls = ServicosFactory.getLoginServicos();
            ArrayList<LoginVO> logins = ls.buscarTodosLogins();
            return logins.isEmpty();
        } catch (Exception e) {
            return true; // Se der erro, assume primeira execu√ß√£o
        }
    }
    
    private void abrirCadastroLogin() {
        try {
            boolean primeiraExecucao = isPrimeiraExecucao();

            // ‚úÖ Abre o Menu Principal Especial
            GUIMenuPrincipalEspecial menuEspecial = new GUIMenuPrincipalEspecial(this, primeiraExecucao);
            menuEspecial.setVisible(true);
            menuEspecial.setExtendedState(JFrame.MAXIMIZED_BOTH);
            this.setVisible(false);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao abrir cadastro: " + e.getMessage());
            e.printStackTrace();
        }
    }
    
